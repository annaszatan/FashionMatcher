import os
import pandas as pd
from PIL import Image
from torch.utils.data import Dataset


def _to_int_or_value(value, default=-1):
    if pd.isna(value):
        return default
    try:
        return int(value)
    except (TypeError, ValueError):
        return value


def _to_str_or_default(value, default=""):
    if pd.isna(value):
        return default
    return str(value)


class ClothingRetrievalDataset(Dataset):
    def __init__(self, metadata_csv, root_dir=".", split="gallery", transform=None):
        """
        metadata_csv: path to metadata_bbox.csv generated by build_metadata
        root_dir: root path to prepend to image_path (for example, 'data' or 'DeepFashion2/train')
        split: 'gallery', 'query', or 'train' (if needed)
        transform: torchvision transforms (for example, preprocessing for DINOv2)
        """
        self.root_dir = root_dir
        self.transform = transform

        df = pd.read_csv(metadata_csv)

        # Filter to the requested split only
        if "split" in df.columns:
            df = df[df["split"] == split].reset_index(drop=True)
        else:
            # If split information is unavailable, use the full dataset
            df = df.reset_index(drop=True)

        self.df = df

    def __len__(self):
        return len(self.df)

    def __getitem__(self, idx):
        row = self.df.iloc[idx]

        # Assume image_path is stored as a relative path in metadata.csv
        # Example: 'image/000001.jpg'
        image_path = os.path.join(self.root_dir, row["image_path"])

        # Bounding box coordinates (cast to int because CSV values may be read as floats)
        x1 = int(row["x1"])
        y1 = int(row["y1"])
        x2 = int(row["x2"])
        y2 = int(row["y2"])
        box = (x1, y1, x2, y2)

        image = Image.open(image_path).convert("RGB")
        crop = image.crop(box)

        if self.transform is not None:
            crop = self.transform(crop)

        # Build label/target information
        target = {
            "source": _to_int_or_value(row["source"], default="unknown") if "source" in row else "unknown",
            "category": _to_int_or_value(row["category"], default=-1) if "category" in row else -1,
            "style": _to_int_or_value(row["style"], default=-1) if "style" in row else -1,
            "pair_id": _to_int_or_value(row["pair_id"], default=-1) if "pair_id" in row else -1,
            "occlusion": _to_int_or_value(row["occlusion"], default=-1) if "occlusion" in row else -1,
            "item_id": _to_str_or_default(row["item_id"], default="") if "item_id" in row else "",
            "image_path": _to_str_or_default(row["image_path"], default=""),
        }

        return crop, target



